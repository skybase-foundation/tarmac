#!/usr/bin/env node
// Single writer for corpus-sync provenance, shared by BOTH sync paths:
//   - apps/webapp/scripts/sync-content.sh (the script)
//   - the /sync-corpus skill (.claude/skills/sync-corpus)
// so both emit byte-identical output. See memory: project-corpus-sync-dual-path.
//
// Writes two artifacts:
//   1. src/data/version.ts          — the current-state pointer (consumed at runtime by App.tsx)
//   2. src/data/corpus-sync-log.jsonl — append-only ledger, one line per sync
//
// Usage:
//   node record-corpus-sync.mjs --branch <ref> --commit <sha> [--tag <tag>] \
//     [--file-types banners,faqs,tooltips] [--changed path1,path2] [--in-sync 5] [--dry-run]

import { writeFileSync, appendFileSync, readFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

// Resolve output paths relative to THIS file, not the caller's cwd — the script runs
// from apps/webapp, the skill runs from the repo root; both must hit the same files.
const dataDir = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'data');
const VERSION_TS = join(dataDir, 'version.ts');
const LOG_JSONL = join(dataDir, 'corpus-sync-log.jsonl');

// --- parse args -------------------------------------------------------------
const argv = process.argv.slice(2);
const opts = {};
for (let i = 0; i < argv.length; i++) {
  const a = argv[i];
  if (!a.startsWith('--')) continue;
  const key = a.slice(2);
  if (key === 'dry-run') {
    opts.dryRun = true;
  } else {
    opts[key] = argv[++i];
  }
}

const branch = opts.branch;
if (!branch) {
  console.error('record-corpus-sync: --branch is required');
  process.exit(1);
}
const commit = opts.commit || null;
// `untagged` is sync-content.sh's own fallback when git describe finds nothing — keep it as-is.
const tag = opts.tag && opts.tag.length ? opts.tag : null;
const fileTypes = opts['file-types']
  ? opts['file-types'].split(',').map(s => s.trim()).filter(Boolean)
  : ['banners', 'faqs', 'tooltips'];

// result is optional: the script overwrites everything (no per-section diff), so it omits these.
// The skill computes a semantic diff and passes them.
const changed = opts.changed ? opts.changed.split(',').map(s => s.trim()).filter(Boolean) : null;
const inSync = opts['in-sync'] != null ? Number(opts['in-sync']) : null;
const result =
  changed === null && inSync === null
    ? null
    : { changed: changed ? changed.length : null, inSync, filesChanged: changed || [] };

// --- render version.ts ------------------------------------------------------
const versionTs = `// Current corpus content pointer — consumed at runtime (see App.tsx).
// Generated by scripts/record-corpus-sync.mjs on each sync; do not edit by hand.
// Full sync history (per-sync commit + affected sections) lives in ./corpus-sync-log.jsonl.

export const CORPUS_VERSION = '${tag ?? 'untagged'}';

export const CORPUS_BRANCH = '${branch}';

export const CORPUS_COMMIT = '${commit ?? 'unknown'}';
`;

// --- render the ledger line -------------------------------------------------
const entry = {
  date: new Date().toISOString(),
  branch,
  version: tag,
  commit,
  fileTypes,
  result,
  backfilled: false
};
const logLine = JSON.stringify(entry);

// --- write (or preview) -----------------------------------------------------
if (opts.dryRun) {
  console.log('--- version.ts (preview) ---');
  console.log(versionTs);
  console.log('--- corpus-sync-log.jsonl (append, preview) ---');
  console.log(logLine);
  process.exit(0);
}

writeFileSync(VERSION_TS, versionTs);
// Append-only: never rewrite existing lines. Ensure the file ends with a newline first.
if (existsSync(LOG_JSONL)) {
  const cur = readFileSync(LOG_JSONL, 'utf8');
  if (cur.length && !cur.endsWith('\n')) appendFileSync(LOG_JSONL, '\n');
}
appendFileSync(LOG_JSONL, logLine + '\n');

console.log(`record-corpus-sync: wrote version.ts (branch=${branch}, tag=${tag ?? 'untagged'}, commit=${(commit ?? 'unknown').slice(0, 8)}) and appended 1 log entry.`);
